/**
 * Parses a CSV string and populates a nested JavaScript object.
 * The CSV is expected to have specific headers for nesting:
 * "Type jour", "Période", "Hypothèse VFR", "Heure UTC", "Nombre d'agents SIV"
 * The 'Nombre d'agents SIV' value will be converted to an integer.
 *
 * @param {string} csvString The raw CSV data as a string.
 * @param {object} targetObject The object to populate.
 */
function populateSivRules(csvString, targetObject) {
  const lines = csvString.trim().split('\n');
  const headers = lines[0].split(';'); // ["Type jour", "Période", "Hypothèse VFR", "Heure UTC", "Nombre d'agents SIV"]

  for (let i = 1; i < lines.length; i++) {
    const values = lines[i].split(';');

    // Skip malformed rows
    if (values.length !== headers.length) {
      console.warn(`Skipping malformed row (incorrect number of columns): ${lines[i]}`);
      continue;
    }

    const day = values[0].trim();
    const period = values[1].trim();
    const vfrHypothesis = values[2].trim();
    const hour = values[3].trim();
    const agentsCount = parseInt(values[4].trim(), 10); // Ensure it's a number

    // Ensure the nested structure exists before assigning the value
    if (!targetObject[day]) {
      targetObject[day] = {};
    }
    if (!targetObject[day][period]) {
      targetObject[day][period] = {};
    }
    if (!targetObject[day][period][vfrHypothesis]) {
      targetObject[day][period][vfrHypothesis] = {};
    }

    // Assign the agents count to the specific hour
    targetObject[day][period][vfrHypothesis][hour] = agentsCount;
  }
}

